package faculdade.to;

public class TOMateria {
    private int id;
    private String nome;
    private String descricao;
    private int idDiscplina;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public int getIdDiscplina() {
        return idDiscplina;
    }

    public void setIdDiscplina(int idDiscplina) {
        this.idDiscplina = idDiscplina;
    }
}
